def decrypt(encrypted_result):
    decrypted = []
    for byte in encrypted_result:
        decrypted_byte = byte ^ 0x57  # Perform XOR decryption
        decrypted.append(decrypted_byte)
    return bytes(decrypted).decode('utf-8')

encrypted_result = [
    0x1f, 0x0f, 0x14, 0x03, 0x11, 0x2c, 0x00, 0x32, 0x3b, 0x34,
    0x38, 0x3a, 0x32, 0x08, 0x23, 0x38, 0x08, 0x03, 0x3f, 0x32,
    0x08, 0x16, 0x04, 0x04, 0x12, 0x1a, 0x15, 0x1b, 0x0e, 0x08,
    0x00, 0x18, 0x05, 0x1b, 0x13, 0x76, 0x2a
]

print(decrypt(encrypted_result))
